<script>
function chooseRandomly() {
  choices = document.getElementById("choicelist").value.split("\n");
  if (choices.length == 0) return;
  document.getElementById("result").value = choices[Math.floor(Math.random() * choices.length)];
}
</script>
<body>
<form action="" onsubmit="return false;">
<textarea id="choicelist" rows=10 cols=80>
</textarea><br><br>
<input type="button" value="Choose" onclick="chooseRandomly(); return false;"><br><br>
<input id="result" type="text" size="80" value="" readonly="readonly"><br><br>
</form>
</body>
